    private void contaEquipamentos(ArrayList<Agent> agents) {
        int workst = 0;
        int server = 0;
        for (Agent agent : agents) {
            if (agent.getMonitoringType().equals("server")) {
                server += 1;
            } else {
                workst += 1;
            }
        }
        lblClient.setText(agents.get(0).getClient());
        lblWrks.setText(String.valueOf(workst));
        lblServer.setText(String.valueOf(server));

    }

